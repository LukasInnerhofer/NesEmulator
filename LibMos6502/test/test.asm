.SEGMENT "INES_HEADER"
.BYTE "NES", $1A
.BYTE $02, $01, $01, $00
.RES 8, $00

.SEGMENT "CODE"
.PROC Code
    LDA #$01
    STA $00
    LDA #$FF
    LDA $00

    LDX #$02
    LDA #$02
    STA $02
    LDA #$FF
    LDA $00,X
Loop:
    NOP
    JMP Loop
.ENDPROC
.RES $7FFA - .SIZEOF(Code), $00

.SEGMENT "NMI_VECTOR"
.ADDR $0000

.SEGMENT "RESET_VECTOR"
.ADDR Code

.SEGMENT "IRQ_VECTOR"
.ADDR $0000

.SEGMENT "CHRROM"
.RES $2000, $AA